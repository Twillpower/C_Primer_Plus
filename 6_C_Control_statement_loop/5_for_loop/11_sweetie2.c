#include <stdio.h>

int main(int argc, char const *argv[])
{
    /* code */
    const int NUMBER = 22;
    int count;
    
    for (count = 1; count <= NUMBER; count++)
    {
        /* code */
        printf("Be my Valentine!\n");
    }
    

    return 0;
}
